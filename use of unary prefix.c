#include <stdio.h>
#include <conio.h>
int main()
{
    int num=4;
    printf ("\n The value of num = %d", num);
    printf ("\n the value of ++num = %d", ++num);
    printf ("\n the value of new num = %d", num);

    printf ("\n The value of num = %d", num);
    printf ("\n The value of --num = %d", --num);
    printf ("\n the value of new num = %d", num);
    return 0;
}
